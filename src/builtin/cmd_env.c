#include "../../include/minishell.h"

int cmd_env(char **arg)
{
    int i;

    i = 0;
    while(g_sh.env[i])
    {
        ft_putstr_fd(g_sh.env[i], 1);
        write(1, "\n", 1);
        i++;
    }
    return(0);
}